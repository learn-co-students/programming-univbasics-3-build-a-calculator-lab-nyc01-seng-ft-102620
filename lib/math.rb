def addition(num1, num2)
  sum = num2 + num1
  sum
end

def subtraction(num1, num2)
  return num1 - num2
end

def division(num1, num2)
  div = num1 / num2
  div
end

def multiplication(num1, num2)
  mult = num1 * num2
  mult
end

def modulo(num1, num2)
  num1 % num2
end

def square_root(num)
  Math.sqrt(num)
end